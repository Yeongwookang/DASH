package com.sp.app.chargestation;

public class ChargeStation {
	private int cStNum;
	private String x_pos;
	private String y_pos;
	private String name;
	private int leftQty;
	private String addr;
	public int getcStNum() {
		return cStNum;
	}
	public void setcStNum(int cStNum) {
		this.cStNum = cStNum;
	}
	public String getX_pos() {
		return x_pos;
	}
	public void setX_pos(String x_pos) {
		this.x_pos = x_pos;
	}
	public String getY_pos() {
		return y_pos;
	}
	public void setY_pos(String y_pos) {
		this.y_pos = y_pos;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getLeftQty() {
		return leftQty;
	}
	public void setLeftQty(int leftQty) {
		this.leftQty = leftQty;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	
	
	
	
	
}
