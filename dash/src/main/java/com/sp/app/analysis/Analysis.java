package com.sp.app.analysis;

public class Analysis {
	private long total_sales;
	private int newCustomerCount;
	private int repairCount;
	private int rentalCount;
	
	public long getTotal_sales() {
		return total_sales;
	}
	public void setTotal_sales(long total_sales) {
		this.total_sales = total_sales;
	}
	public int getNewCustomerCount() {
		return newCustomerCount;
	}
	public void setNewCustomerCount(int newCustomerCount) {
		this.newCustomerCount = newCustomerCount;
	}
	public int getRepairCount() {
		return repairCount;
	}
	public void setRepairCount(int repairCount) {
		this.repairCount = repairCount;
	}
	public int getRentalCount() {
		return rentalCount;
	}
	public void setRentalCount(int rentalCount) {
		this.rentalCount = rentalCount;
	}
	
	
}
