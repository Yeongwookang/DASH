package com.sp.app.register;

public class Register {
	private String name;
	private String x_pos;
	private String y_pos;
	private int maxQty;
	private String zoonName;
	private String addr;
	
	private int LEFTQTY;
	private String state;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getX_pos() {
		return x_pos;
	}
	public void setX_pos(String x_pos) {
		this.x_pos = x_pos;
	}
	public String getY_pos() {
		return y_pos;
	}
	public void setY_pos(String y_pos) {
		this.y_pos = y_pos;
	}
	public int getMaxQty() {
		return maxQty;
	}
	public void setMaxQty(int maxQty) {
		this.maxQty = maxQty;
	}
	public String getZoonName() {
		return zoonName;
	}
	public void setZonName(String zonName) {
		this.zoonName = zonName;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public int getLEFTQTY() {
		return LEFTQTY;
	}
	public void setLEFTQTY(int lEFTQTY) {
		LEFTQTY = lEFTQTY;
	}
	
	
}
