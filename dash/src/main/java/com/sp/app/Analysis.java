package com.sp.app;

public class Analysis {
	private int usageRank;
	private int usageCount;
	private String name;
	
	private int salesRank;
	private int price;
	private String zoonName;
	
	public int getUsageRank() {
		return usageRank;
	}
	public void setUsageRank(int usageRank) {
		this.usageRank = usageRank;
	}
	public int getUsageCount() {
		return usageCount;
	}
	public void setUsageCount(int usageCount) {
		this.usageCount = usageCount;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getSalesRank() {
		return salesRank;
	}
	public void setSalesRank(int salesRank) {
		this.salesRank = salesRank;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getZoonName() {
		return zoonName;
	}
	public void setZoonName(String zoonName) {
		this.zoonName = zoonName;
	}
	
	
	
	
	
	
}
